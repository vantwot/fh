const Database = require('better-sqlite3');
const path = require('path');

const db = new Database(path.join(__dirname, 'database.sqlite'));

db.exec(`
  CREATE TABLE IF NOT EXISTS users (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    username TEXT UNIQUE NOT NULL,
    password TEXT NOT NULL,
    role TEXT DEFAULT 'admin'
  );

  CREATE TABLE IF NOT EXISTS sales (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    date TEXT NOT NULL,
    category TEXT NOT NULL,
    description TEXT,
    amount REAL NOT NULL,
    payment_method TEXT NOT NULL,
    client_name TEXT,
    phone TEXT,
    system_code TEXT,
    membership_type TEXT
  );

  CREATE TABLE IF NOT EXISTS inventory (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    category TEXT NOT NULL,
    stock INTEGER DEFAULT 0,
    price REAL DEFAULT 0
  );

  CREATE TABLE IF NOT EXISTS teachers (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    disciplines TEXT, -- JSON string
    hourly_rate REAL DEFAULT 0
  );

  CREATE TABLE IF NOT EXISTS teacher_logs (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    teacher_id INTEGER NOT NULL,
    hours REAL NOT NULL,
    description TEXT,
    date TEXT NOT NULL,
    FOREIGN KEY (teacher_id) REFERENCES teachers(id)
  );

  CREATE TABLE IF NOT EXISTS expenses (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    type TEXT NOT NULL, -- academia, personal
    category TEXT NOT NULL,
    description TEXT,
    amount REAL NOT NULL,
    payment_method TEXT NOT NULL,
    date TEXT NOT NULL
  );

  CREATE TABLE IF NOT EXISTS memberships (
    id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    number_duration INTEGER,
    type_duration TEXT,
    cost REAL,
    is_promotional INTEGER,
    date_expired TEXT,
    number_person INTEGER,
    is_active INTEGER,
    monday INTEGER,
    tuesday INTEGER,
    wednesday INTEGER,
    thursday INTEGER,
    friday INTEGER,
    saturday INTEGER,
    sunday INTEGER,
    auditory TEXT,
    expired_type_duration TEXT,
    expired_number_duration INTEGER,
    contains_class INTEGER,
    number_class INTEGER,
    observation TEXT,
    year_init INTEGER,
    year_end INTEGER,
    subsidy TEXT,
    multi_sede INTEGER
  );

  CREATE TABLE IF NOT EXISTS members (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    identification TEXT UNIQUE,
    phone TEXT,
    email TEXT,
    birth_date TEXT,
    status TEXT DEFAULT 'Activo',
    registration_date TEXT,
    membership_id TEXT,
    photo TEXT, -- Base64 image
    fingerprint TEXT, -- Fingerprint template or hash
    FOREIGN KEY (membership_id) REFERENCES memberships(id)
  );

  CREATE TABLE IF NOT EXISTS membership_payments (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    member_id INTEGER NOT NULL,
    membership_id TEXT NOT NULL,
    total_value REAL NOT NULL,
    discount REAL DEFAULT 0,
    registration_fee REAL DEFAULT 0,
    amount_paid REAL NOT NULL,
    payment_method TEXT NOT NULL, -- efectivo, transferencia
    observation TEXT,
    date TEXT NOT NULL,
    start_date TEXT,
    end_date TEXT,
    visits INTEGER,
    FOREIGN KEY (member_id) REFERENCES members(id),
    FOREIGN KEY (membership_id) REFERENCES memberships(id)
  );
`);

